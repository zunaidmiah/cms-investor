                  <h2 class="red-title"><span>Board Structure</span></h2>
                  <h5>a. Composition</h5>
                  <p>The number of directors shall not be less than 2 not more than 15 as set out in the Company's Articles of Association.</p>
                  <p>In accordance with the Ace Market Listing Requirements of Bursa Malaysia Securities Berhad, the Company must ensure that at least 2 directors or 1/3 of the Board, whichever is the higher, are independent directors. If the number of directors of the Company is not 3 or a multiple of 3, then the number nearest 1/3 must be used.</p>
                  <h5>b. Appointment and Re-election</h5>
                  <p>The Board is responsible to determine the appropriate size of the Board and the appointment of new director is a matter for consideration and decision by the Board, upon the recommendation from the Nominating Committee ("NC"). The NC will consider the required mix of skills, experiences, other qualities and diversity, including gender, where appropriate, which the Director should bring to the Board.</p>
                  <p>In accordance with the Company's Articles of Association, one-third (1/3) or the number nearest to one-third (1/3) shall retire from office at each Annual General Meeting. A retiring Director is eligible for re-appointment. It provides that any new or additional Director appointed by the Board during the year shall hold office until the next Annual General Meeting and shall then be eligible for re-election. The election of each Director is voted separately.</p>
