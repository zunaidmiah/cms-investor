                <div class="span12">
                   <h5>The Performance of the DEL Market in Malaysia</h5>
                   <p>The Malaysian Communications and Multimedia Commission ("MCMC") defines DEL as fixed line that connects a customer's equipment to the Public Switched Telephone Network ("PSTN") and has a dedicated port on a telephone exchange. In Malaysia, number of subscriptions for DEL decreased in 2011 with a total of 4,091,000 compared to 4,406,000 subscriptions recorded in 2010. Meanwhile, the number of business DEL subscriptions has continued to grow over the years as more companies use the fixed line for general contact and fax purpose.</p>
                   <h5>The Performance of the Cellular Telephones Market in Malaysia</h5>
                   <p>The performance of the cellular telephones market in Malaysia remains promising and is clearly heading in the right direction. The number of cellular telephone subscriptions increased by 9.8 percent year-on-year from 33,859,000 in 2010 to 36,662,000 in 2011.</p>
                   <h5>The Performance of the Broadband and the Internet Market in Malaysia</h5>
                   <p>The broadband and the Internet market in Malaysia is another bright spot in the local ICT industry with strong contribution from the broadband market. Total number of broadband subscriptions in Malaysia surged from 755,500 in 2006 to more than 5.6 million in 2011 on the back of favourable government policies such as the MyICMS886 and aggressive rollouts of broadband infrastructure by telecommunications carriers or operators.</p>
                  
